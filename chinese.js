function makeChineseExplanation(item) {
  const hanzi = String(item.hanzi || "");
  const chars = [...hanzi].filter((char) => CHINESE_PARTS[char]);

  if (!chars.length) {
    return `${hanzi} — китайское слово. Значение: ${item.russian || item.meaning || ""}.`;
  }

  const parts = chars
    .map((char) => `${char} — ${CHINESE_PARTS[char]}`)
    .join(", ");

  return `${parts}. Вместе ${hanzi} значит: ${item.russian || item.meaning || ""}.`;
}

